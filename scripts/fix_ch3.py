"""Strip e-commerce leftovers from chapter 3 (economic part)."""
from docx import Document

PATH = "dist/Диплом_Ксёнжик_исправленный.docx"


def set_text(p, text):
    runs = p.runs
    if not runs:
        p.add_run(text); return
    first = runs[0]
    for r in runs[1:]:
        r._element.getparent().remove(r._element)
    first.text = text


def find(doc, contains):
    for p in doc.paragraphs:
        if contains in p.text:
            return p
    return None


def main():
    doc = Document(PATH)

    p = find(doc, "Для расчета годового экономического эффекта примем")
    if p:
        set_text(p,
            "Для расчёта годового экономического эффекта примем, что после внедрения системы "
            "редакция экономит 20 часов рабочего времени в месяц за счёт автоматизации "
            "публикации новостей, ведения рубрикатора, модерации комментариев и обработки "
            "обращений читателей. Часовая стоимость сотрудника принимается равной средней "
            "часовой ставке журналиста-редактора с учётом начислений на заработную плату.")

    p = find(doc, "Внедрение веб-приложения повышает эффективность работы организации")
    if p:
        set_text(p,
            "Внедрение веб-приложения повышает эффективность работы организации по нескольким "
            "направлениям. Сокращается время на публикацию новостных материалов, поскольку "
            "редактор работает в едином интерфейсе с категориями, изображениями и текстом "
            "статьи. Уменьшается нагрузка на сотрудников отдела сопровождения, поскольку "
            "пользователи самостоятельно регистрируются, ведут личный кабинет, оставляют "
            "комментарии и сохраняют публикации в закладки без участия персонала.")

    p = find(doc, "Для предприятия это означает")
    if p:
        set_text(p, "Для предприятия это означает:")

    fixes_list = {
        "уменьшение трудоемкости обработки заказов":
            "уменьшение трудоёмкости публикации и сопровождения новостных материалов; ",
        "сокращение времени ответа на обращения":
            "сокращение времени реакции на обращения и комментарии читателей; ",
        "повышение точности учета данных":
            "повышение точности учёта пользовательской активности (просмотры, комментарии, закладки); ",
        "рост удобства работы с клиентами":
            "рост удобства работы с аудиторией за счёт интерактивных функций; ",
        "улучшение управляемости каталога и заказов":
            "улучшение управляемости рубрикатором новостей и контентом в целом. ",
    }
    for needle, new in fixes_list.items():
        pp = find(doc, needle)
        if pp:
            set_text(pp, new)

    p = find(doc, "С точки зрения экономической эффективности такая система не только окупает")
    if p:
        set_text(p,
            "С точки зрения экономической эффективности разработанная система не только "
            "окупает вложения в разработку, но и формирует технологическую основу для "
            "дальнейшего роста охвата аудитории, повышения качества обслуживания читателей "
            "и развития новых направлений работы новостного агентства.")

    doc.save(PATH)
    print("Chapter 3 cleaned.")


if __name__ == "__main__":
    main()
