async function seedArticles() {
  const client = await pool.connect();

  try {
    // Get admin user
    const adminResult = await client.query(`SELECT id FROM users WHERE is_admin = true LIMIT 1`);
    if (adminResult.rows.length === 0) {
      console.log("No admin user found. Please run the main seed script first.");
      return;
    }
    const adminId = adminResult.rows[0].id;
    console.log("Using admin user:", adminId);

    // Get categories
    const catResult = await client.query(`SELECT id, slug FROM categories`);
    if (catResult.rows.length === 0) {
      console.log("No categories found. Please run the main seed script first.");
      return;
    }
    const categoryMap: Record<string, string> = {};
    for (const row of catResult.rows) {
      categoryMap[row.slug] = row.id;
    }
    console.log("Categories found:", Object.keys(categoryMap));

    // Check existing news count
    const existingCount = await client.query(`SELECT COUNT(*) as count FROM news`);
    console.log("Existing news count:", existingCount.rows[0].count);

    let inserted = 0;
    for (const article of articles) {
      const categoryId = categoryMap[article.category];
      if (!categoryId) {
        console.log(`Category not found: ${article.category}`);
        continue;
      }

      // Check if article with same title already exists
      const existing = await client.query(`SELECT id FROM news WHERE title = $1 LIMIT 1`, [article.title]);
      if (existing.rows.length > 0) {
        console.log(`Article already exists: ${article.title.substring(0, 50)}...`);
        continue;
      }

      const randomDaysAgo = Math.floor(Math.random() * 60) + 1;
      const createdAt = new Date(Date.now() - randomDaysAgo * 24 * 60 * 60 * 1000);
      const views = Math.floor(Math.random() * 2000) + 50;

      await client.query(`
        INSERT INTO news (title, excerpt, content, category_id, image_url, author_id, views, created_at, updated_at)
        VALUES ($1, $2, $3, $4, $5, $6, $7, $8, $8)
      `, [
        article.title,
        article.excerpt,
        article.content,
        categoryId,
        article.imageUrl,
        adminId,
        views,
        createdAt,
      ]);

      inserted++;
      console.log(`Inserted: ${article.title.substring(0, 60)}...`);
    }

    console.log(`\nDone! Inserted ${inserted} new articles.`);
  } finally {
    client.release();
    await pool.end();
  }
}

